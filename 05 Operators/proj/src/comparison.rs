pub fn comparison() {
    let a = 5;
    let b = 10;

    // Equality
    println!("Equality (a == b): {}", a == b); // Output: false

    // Inequality
    println!("Inequality (a != b): {}", a != b); // Output: true

    // Greater Than
    println!("Greater Than (a > b): {}", a > b); // Output: false

    // Less Than
    println!("Less Than (a < b): {}", a < b); // Output: true

    // Greater Than or Equal To
    println!("Greater Than or Equal To (a >= b): {}", a >= b); // Output: false

    // Less Than or Equal To
    println!("Less Than or Equal To (a <= b): {}", a <= b); // Output: true
}
